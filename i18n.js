// Get translation with fallback
function t(key) {
  const lang = document.documentElement.lang === 'en' ? 'en' : 'de';
  return translations[lang][key] || `[${key}]`;
}

// Update all elements with data-i18n attribute
function updatePageTranslations() {
  const lang = document.documentElement.lang || 'de';
  
  document.querySelectorAll('[data-i18n]').forEach(elem => {
    const key = elem.dataset.i18n;
    const text = translations[lang][key];
    
    if (!text) return;
    
    // Determine how to set the text
    if (elem.tagName === 'TITLE') {
      elem.textContent = text;
    } else if (elem.dataset.i18nHtml === 'true' || key.includes('Text') || key.includes('Intro') || key.includes('text')) {
      elem.innerHTML = text;
    } else {
      elem.textContent = text;
    }
  });
  
  // Update language switcher
  const langLabel = document.getElementById('lang-label');
  const langDE = document.getElementById('lang-de');
  const langEN = document.getElementById('lang-en');
  
  if (langLabel) {
    langLabel.textContent = lang === 'en' ? translations.en.heroLangLabel : translations.de.heroLangLabel;
  }
  
  if (langDE && langEN) {
    if (lang === 'de') {
      // German is active
      langDE.style.fontWeight = 'bold';
      langDE.style.color = '#fff';
      langDE.style.cursor = 'default';
      langEN.style.fontWeight = 'normal';
      langEN.style.color = '#aaa';
      langEN.style.cursor = 'pointer';
    } else {
      // English is active
      langDE.style.fontWeight = 'normal';
      langDE.style.color = '#aaa';
      langDE.style.cursor = 'pointer';
      langEN.style.fontWeight = 'bold';
      langEN.style.color = '#fff';
      langEN.style.cursor = 'default';
    }
  }
}

// Switch language
function switchLanguage(lang) {
  document.documentElement.lang = lang;
  localStorage.setItem('language', lang);
  updatePageTranslations();
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('language');
  if (saved && saved !== document.documentElement.lang) {
    document.documentElement.lang = saved;
  }
  updatePageTranslations();
  
  // Language switcher: DE link
  const langDE = document.getElementById('lang-de');
  if (langDE) {
    langDE.addEventListener('click', () => {
      if (document.documentElement.lang !== 'de') {
        switchLanguage('de');
      }
    });
  }
  
  // Language switcher: EN link
  const langEN = document.getElementById('lang-en');
  if (langEN) {
    langEN.addEventListener('click', () => {
      if (document.documentElement.lang !== 'en') {
        switchLanguage('en');
      }
    });
    
    // Keyboard support for EN
    langEN.addEventListener('keydown', (e) => {
      if (e.key === 'Enter' || e.key === ' ') {
        if (document.documentElement.lang !== 'en') {
          switchLanguage('en');
        }
      }
    });
  }
});
